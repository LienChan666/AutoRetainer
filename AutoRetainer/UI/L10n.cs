    private static IReadOnlyDictionary<string, string> BuildZhCn()
    {
        var merged = new Dictionary<string, string>(StringComparer.Ordinal);
        AddTranslations(merged, ZhCnSymbolsAndNumbers);
        AddTranslations(merged, ZhCnAtoE);
        AddTranslations(merged, ZhCnFtoL);
        AddTranslations(merged, ZhCnMtoR);
        AddTranslations(merged, ZhCnStoZ);
        return merged;
    }

    private static void AddTranslations(Dictionary<string, string> target, IReadOnlyDictionary<string, string> source)
    {
        foreach (var (key, value) in source)
        {
            if (target.TryGetValue(key, out var existing))
            {
                if (!StringComparer.Ordinal.Equals(existing, value))
                {
                    PluginLog.Warning($"Conflicting zh-CN translation for \"{key}\": keeping \"{existing}\", ignoring \"{value}\".");
                }

                continue;
            }

            target.Add(key, value);
        }
    }

    private static IReadOnlyDictionary<string, string> BuildZhCnNormalized()
    {
        var normalized = new Dictionary<string, string>(StringComparer.Ordinal);
        foreach (var (key, value) in ZhCn)
        {
            AddNormalizedVariant(normalized, key, value);
            AddNormalizedVariant(normalized, UnescapeControlChars(key), value);
        }
        return normalized;
    }

    private static void AddNormalizedVariant(Dictionary<string, string> target, string source, string translated)
    {
        var normalized = NormalizeWhitespace(source);
        if (normalized.Length == 0)
            return;
        target[normalized] = translated;
    }

