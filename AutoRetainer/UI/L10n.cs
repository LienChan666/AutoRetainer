    internal static string Tr(string key)
    {
        if (key.Length == 0)
            return key;

        var (display, suffix) = SplitImGuiLabel(key);
        if (display.Length == 0)
            return key;

        var translated = TranslateDisplay(display);
        return translated + suffix;
    }

    internal static string TrMenuSegment(string segment)
    {
        if (segment.Length == 0)
            return segment;

        if (TryTranslateExact(segment, out var fromZhCn))
            return fromZhCn;

        return MenuSectionsZhCn.TryGetValue(segment, out var zh) ? zh : segment;
    }

    internal static string TrMenuPath(string path)
    {
        if (path.Length == 0)
            return path;

        if (TryTranslateExact(path, out var fromZhCn))
            return fromZhCn;

        if (MenuPathsZhCn.TryGetValue(path, out var zhPath))
            return zhPath;

        var parts = path.Split('/');
        for (var i = 0; i < parts.Length; i++)
        {
            parts[i] = TrMenuSegment(parts[i]);
        }
        return string.Join("/", parts);
    }

    internal static string Tr(string key, params object[] args)
        => string.Format(CultureInfo.CurrentCulture, Tr(key), args);

    private static string TranslateDisplay(string text)
    {
        if (TryTranslateExact(text, out var exact))
            return exact;

        if (!text.Contains('\n'))
            return text;

        var lines = text.Split('\n');
        var changed = false;
        for (var i = 0; i < lines.Length; i++)
        {
            var line = lines[i];
            var core = line;
            var hadCr = core.EndsWith('\r');
            if (hadCr)
                core = core[..^1];

            if (TryTranslateExact(core, out var translatedLine))
            {
                lines[i] = hadCr ? translatedLine + "\r" : translatedLine;
                changed = true;
                continue;
            }

            var leading = 0;
            while (leading < core.Length && char.IsWhiteSpace(core[leading]))
                leading++;

            if (leading > 0)
            {
                var prefix = core[..leading];
                var body = core[leading..];
                if (TryTranslateExact(body, out var translatedBody))
                {
                    lines[i] = hadCr ? prefix + translatedBody + "\r" : prefix + translatedBody;
                    changed = true;
                }
            }
        }

        return changed ? string.Join("\n", lines) : text;
    }

    private static bool TryTranslateExact(string text, out string translated)
    {
        if (ZhCn.TryGetValue(text, out var exact))
        {
            translated = UnescapeControlChars(exact);
            return true;
        }

        var escaped = EscapeControlChars(text);
        if (!ReferenceEquals(escaped, text) && ZhCn.TryGetValue(escaped, out var escapedExact))
        {
            translated = UnescapeControlChars(escapedExact);
            return true;
        }

        var normalized = NormalizeWhitespace(text);
        if (normalized.Length > 0 && ZhCnNormalized.TryGetValue(normalized, out var normalizedExact))
        {
            translated = UnescapeControlChars(normalizedExact);
            return true;
        }

        var normalizedUnescaped = NormalizeWhitespace(UnescapeControlChars(text));
        if (normalizedUnescaped.Length > 0 && !ReferenceEquals(normalizedUnescaped, normalized) && ZhCnNormalized.TryGetValue(normalizedUnescaped, out var normalizedUnescapedExact))
        {
            translated = UnescapeControlChars(normalizedUnescapedExact);
            return true;
        }

        translated = string.Empty;
        return false;
    }

    private static string NormalizeWhitespace(string text)
    {
        if (text.Length == 0)
            return text;

        var sb = new StringBuilder(text.Length);
        var pendingSpace = false;
        foreach (var c in text)
        {
            if (char.IsWhiteSpace(c))
            {
                if (sb.Length > 0)
                    pendingSpace = true;
                continue;
            }

            if (pendingSpace)
            {
                sb.Append(' ');
                pendingSpace = false;
            }

            sb.Append(c);
        }
        return sb.ToString();
    }

    private static string EscapeControlChars(string text)
        => text.Replace("\r", "\\r").Replace("\n", "\\n").Replace("\t", "\\t");

    private static string UnescapeControlChars(string text)
        => text.Replace("\\r", "\r").Replace("\\n", "\n").Replace("\\t", "\t");

    private static (string Display, string Suffix) SplitImGuiLabel(string input)
    {
        if (input.StartsWith("##", StringComparison.Ordinal))
            return (string.Empty, input);

        var tripleIdx = input.IndexOf("###", StringComparison.Ordinal);
        if (tripleIdx >= 0)
            return (input[..tripleIdx], input[tripleIdx..]);

        var doubleIdx = input.IndexOf("##", StringComparison.Ordinal);
        if (doubleIdx >= 0)
            return (input[..doubleIdx], input[doubleIdx..]);

        return (input, string.Empty);
    }
